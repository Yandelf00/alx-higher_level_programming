#!/usr/bin/python3
def add(a, b):
    """an add function

    Args:
        a: first int
        b: second int

    Returns:
        Returns the value of a + b
    """
    return (a + b)


def sub(a, b):
    """a sub function

    Args:
        a: first int
        b: second int

    Returns:
        Returns the value of a - b
    """
    return (a - b)


def mul(a, b):
    """a mul function

    Args:
        a: first int
        b: second int

    Returns:
        Returns the value of a * b
    """
    return (a * b)


def div(a, b):
    """a div function

    Args:
        a: first int
        b: second int

    Returns:
        Returns the value of a / b
    """
    return int(a / b)
